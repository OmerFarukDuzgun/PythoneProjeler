
ders = ["B", "İ", "L", "İ", "Ş", "İ", "M"]

# Listenin uzunluğunu bulma
uzunluk = len(ders)
print("Listenin uzunluğu:", uzunluk)

# Listenin ilk elemanını alma
ilk_eleman = ders[0]
print("Listenin ilk elemanı:", ilk_eleman)

# Listenin son elemanını alma
son_eleman = ders[-1]
print("Listenin son elemanı:", son_eleman)

# Listenin belirli bir aralığını alma
belirli_aralik = ders[2:5]
print("Belirli aralık:", belirli_aralik)

# Listenin ters çevrilmiş hali
ters_liste = ders[::-1]
print("Ters çevrilmiş liste:", ters_liste)

